import java.util.Scanner;

public class Senha {
    public static void main (String[] args){

        int senha = 1234;
        int tentativa = 0;

        while(tentativa != senha){
            tentativa = 1234;
            if (tentativa == senha )
            System.out.println("Tentando acessar...");
        }
        System.out.println("Acesso Liberado!");
    }
}
